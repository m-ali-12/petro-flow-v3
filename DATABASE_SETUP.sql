SELECT public._drop_all_policies_v4('expense_categories');

-- 3. Define Clean Policies
CREATE POLICY "up_own_all" ON public.user_profiles FOR ALL TO authenticated USING (auth.uid() = user_id);
CREATE POLICY "up_admin_all" ON public.user_profiles FOR ALL TO authenticated USING (public.check_is_admin());

DO $$ 
BEGIN
    EXECUTE 'CREATE POLICY "customers_access" ON public.customers FOR ALL TO authenticated USING (true)';
    EXECUTE 'CREATE POLICY "transactions_access" ON public.transactions FOR ALL TO authenticated USING (true)';
    EXECUTE 'CREATE POLICY "settings_access" ON public.settings FOR ALL TO authenticated USING (true)';
    EXECUTE 'CREATE POLICY "advances_access" ON public.cash_advances FOR ALL TO authenticated USING (true)';
    EXECUTE 'CREATE POLICY "exp_cat_access" ON public.expense_categories FOR ALL TO authenticated USING (true)';
EXCEPTION WHEN OTHERS THEN NULL;
END $$;

