-- PetroFlow — MULTI-COMPANY ISOLATION & STABILIZATION SCRIPT
-- ================================================================
-- PURPOSE: 
-- 1. Implements Multi-Tenancy (Data Isolation by Company).
-- 2. Adds Invitation System for staff members.
-- 3. Standardizes company_id across all tables.
-- ================================================================

-- STEP 1: CREATE CORE TENANCY TABLES
-- ----------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.companies (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL DEFAULT 'My Petrol Pump',
    owner_id UUID REFERENCES auth.users(id),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.staff_invites (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email TEXT UNIQUE NOT NULL,
    company_id UUID REFERENCES public.companies(id) ON DELETE CASCADE,
    role TEXT NOT NULL DEFAULT 'employee',
    status TEXT DEFAULT 'pending',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT now()
);

-- STEP 2: ALTER TABLES TO ADD company_id
-- ----------------------------------------------------------------
DO $$ 
DECLARE 
    r RECORD;
BEGIN 
    -- 1. Add company_id to user_profiles if missing
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_schema = 'public' AND table_name = 'user_profiles' AND column_name = 'company_id') THEN
        ALTER TABLE public.user_profiles ADD COLUMN company_id UUID REFERENCES public.companies(id);
    END IF;

    -- STEP 2.1: Resolve Naming Conflicts (B2B companies vs Tenants)
    -- Some tables used 'company_id' to refer to a B2B customer (BIGINT). 
    -- We need to rename these to 'b2b_company_id' to free up 'company_id' for multi-tenancy.
    FOR r IN (
        SELECT table_name, column_name 
        FROM information_schema.columns 
        WHERE table_schema = 'public' 
        AND column_name = 'company_id'
        AND data_type IN ('integer', 'bigint')
        AND table_name IN ('company_transactions', 'company_repayments', 'member_card_usage')
    ) LOOP
        BEGIN
            EXECUTE format('ALTER TABLE public.%I RENAME COLUMN company_id TO b2b_company_id', r.table_name);
        EXCEPTION WHEN OTHERS THEN NULL;
        END;
    END LOOP;

    -- STEP 2.2: Ensure company_id is UUID for all isolated tables
    FOR r IN (
        SELECT table_name FROM information_schema.tables WHERE table_schema = 'public' 
        AND table_name IN (
            'customers', 'transactions', 'expense_categories', 'cash_advances', 
            'company_transactions', 'company_repayments', 'mobil_sales', 'mobil_stock',
            'mobil_transactions', 'rent_payments', 'settings', 'shops', 'stock_entries',
            'stock_purchases', 'tanks', 'expense_types', 'member_card_usage', 'mobil_arrivals',
            'bank_accounts', 'banks', 'cash_deposits', 'vendor_payments'
        )
    ) LOOP
        -- If it exists but is NOT UUID, drop it (we'll recreate it correctly)
        IF EXISTS (
            SELECT 1 FROM information_schema.columns 
            WHERE table_schema = 'public' AND table_name = r.table_name AND column_name = 'company_id'
            AND data_type <> 'uuid'
        ) THEN
            EXECUTE format('ALTER TABLE public.%I DROP COLUMN company_id CASCADE', r.table_name);
        END IF;

        -- Now add it as UUID if missing
        IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_schema = 'public' AND table_name = r.table_name AND column_name = 'company_id') THEN
            EXECUTE format('ALTER TABLE public.%I ADD COLUMN company_id UUID REFERENCES public.companies(id)', r.table_name);
        END IF;
        
        -- Set default
        EXECUTE format('ALTER TABLE public.%I ALTER COLUMN company_id SET DEFAULT public.get_my_company()', r.table_name);
    END LOOP;

    -- STEP 2.3: Re-establish Core Relationships (Fix PostgREST joins)
    -- Ensure transactions link to customers
    BEGIN
        ALTER TABLE public.transactions DROP CONSTRAINT IF EXISTS transactions_customer_id_fkey;
        ALTER TABLE public.transactions ADD CONSTRAINT transactions_customer_id_fkey 
            FOREIGN KEY (customer_id) REFERENCES public.customers(id) ON DELETE CASCADE;
    EXCEPTION WHEN OTHERS THEN NULL;
    END;

    -- Ensure cash_advances link to customers
    BEGIN
        ALTER TABLE public.cash_advances DROP CONSTRAINT IF EXISTS cash_advances_customer_id_fkey;
        ALTER TABLE public.cash_advances ADD CONSTRAINT cash_advances_customer_id_fkey 
            FOREIGN KEY (customer_id) REFERENCES public.customers(id) ON DELETE CASCADE;
    EXCEPTION WHEN OTHERS THEN NULL;
    END;

    -- Ensure company_transactions link to customers (B2B)
    BEGIN
        ALTER TABLE public.company_transactions DROP CONSTRAINT IF EXISTS company_transactions_b2b_company_id_fkey;
        ALTER TABLE public.company_transactions ADD CONSTRAINT company_transactions_b2b_company_id_fkey 
            FOREIGN KEY (b2b_company_id) REFERENCES public.customers(id) ON DELETE CASCADE;
    EXCEPTION WHEN OTHERS THEN NULL;
    END;

    -- STEP 2.4: Populate NULL company_id for existing records
    -- This ensures that current data becomes visible to the owner/admin
    FOR r IN (
        SELECT c.table_name FROM information_schema.columns c
        JOIN information_schema.tables t ON t.table_name = c.table_name AND t.table_schema = c.table_schema
        WHERE c.table_schema = 'public' 
        AND c.column_name = 'company_id'
        AND t.table_type = 'BASE TABLE'
    ) LOOP
        -- Only attempt join if user_id column exists in this table
        IF EXISTS (
            SELECT 1 FROM information_schema.columns 
            WHERE table_schema = 'public' AND table_name = r.table_name AND column_name = 'user_id'
        ) THEN
            EXECUTE format('
                UPDATE public.%I t
                SET company_id = p.company_id
                FROM public.user_profiles p
                WHERE t.company_id IS NULL 
                AND t.user_id = p.user_id 
                AND p.company_id IS NOT NULL', r.table_name);
        END IF;
            
        -- Fallback: If still NULL, assign to the first company found
        EXECUTE format('
            UPDATE public.%I 
            SET company_id = (SELECT id FROM public.companies LIMIT 1)
            WHERE company_id IS NULL', r.table_name);
    END LOOP;

    -- STEP 2.5: Auto-Activate existing Admins and assign first company if missing
    UPDATE public.user_profiles 
    SET status = 'active', 
        company_id = COALESCE(company_id, (SELECT id FROM public.companies LIMIT 1))
    WHERE role IN ('admin', 'super_admin') 
    AND (status != 'active' OR company_id IS NULL);

END $$;

