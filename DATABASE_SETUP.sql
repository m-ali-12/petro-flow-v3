-- ================================================================
-- PetroFlow — MASTER DATABASE REPAIR & STABILIZATION SCRIPT
-- ================================================================
-- PURPOSE: 
-- 1. Corrects tables that were created without auto-increment IDs.
-- 2. Standardizes ID columns to BIGINT IDENTITY.
-- 3. Fixes Foreign Key relationships.
-- 4. Rebuilds the Auth Trigger and RLS policies.
-- ================================================================

-- STEP 1: SELF-HEALING IDENTITY FIX (The "Power Block")
-- This block scans ALL public tables and adds "Auto-Increment" to any missing IDs.
-- ----------------------------------------------------------------
DO $$ 
DECLARE 
    r RECORD;
BEGIN 
    -- 1. Fix user_profiles first (Compatibility with Auth)
    IF EXISTS (SELECT 1 FROM information_schema.columns WHERE table_schema = 'public' AND table_name = 'user_profiles' AND column_name = 'id' AND data_type = 'uuid') THEN
       -- If it's a UUID, we keep it as is, but ensure it's the primary key
       NULL;
    ELSE
       -- If it is a BIGINT, ensure it has a sequence or identity
       BEGIN
          ALTER TABLE public.user_profiles ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY;
       EXCEPTION WHEN OTHERS THEN 
          RAISE NOTICE 'user_profiles.id already has identity/sequence';
       END;
    END IF;

    -- 2. Loop through a list of critical tables and force Identity/BigInt
    FOR r IN (
        SELECT table_name 
        FROM information_schema.tables 
        WHERE table_schema = 'public' 
        AND table_name IN (
            'customers', 'transactions', 'expense_categories', 'cash_advances', 
            'company_transactions', 'company_repayments', 'mobil_sales', 'mobil_stock',
            'mobil_transactions', 'rent_payments', 'settings', 'shops', 'stock_entries',
            'stock_purchases', 'tanks', 'expense_types', 'member_card_usage', 'mobil_arrivals',
            'mobil_product_prices', 'mobil_products', 'price_history', 'banks', 'cash_deposits'
        )
    ) LOOP
        -- a. Ensure column is BIGINT
        BEGIN
            EXECUTE format('ALTER TABLE public.%I ALTER COLUMN id SET DATA TYPE BIGINT', r.table_name);
        EXCEPTION WHEN OTHERS THEN
            RAISE NOTICE 'Skipped type cast for % (maybe already bigint or incompatible)', r.table_name;
        END;

        -- b. Add Identity (Auto-Increment)
        BEGIN
            EXECUTE format('ALTER TABLE public.%I ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY', r.table_name);
            RAISE NOTICE 'Repaired Identity for table: %', r.table_name;
        EXCEPTION WHEN OTHERS THEN
            RAISE NOTICE 'Table % already has identity or sequence.', r.table_name;
        END;
    END LOOP;
END $$;

-- STEP 2: CONSTRAINT FIXES
-- ----------------------------------------------------------------
-- Ensure UNIQUE constraint on expense_categories(name)
DO $$ 
BEGIN 
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'expense_categories_name_key') THEN
        ALTER TABLE public.expense_categories ADD CONSTRAINT expense_categories_name_key UNIQUE (name);
    END IF;
EXCEPTION WHEN OTHERS THEN NULL;
END $$;

-- STEP 3: REBUILD AUTH TRIGGER
-- ----------------------------------------------------------------
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER AS $$
BEGIN
  INSERT INTO public.user_profiles (user_id, email, role, status)
  VALUES (new.id, new.email, 'employee', 'pending')
  ON CONFLICT (user_id) DO NOTHING;
  RETURN new;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

-- STEP 4: SYNC EXISTING USERS
-- ----------------------------------------------------------------
INSERT INTO public.user_profiles (user_id, email, role, status)
SELECT id, email, 'employee', 'pending'
FROM auth.users
ON CONFLICT (user_id) DO NOTHING;

