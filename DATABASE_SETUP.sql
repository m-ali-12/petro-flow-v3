DROP POLICY IF EXISTS isolation_policy ON public.staff_invites;
CREATE POLICY isolation_policy ON public.staff_invites FOR ALL TO authenticated USING (
    company_id = public.get_my_company()
    OR email = (SELECT email FROM auth.users WHERE id = auth.uid())
    OR EXISTS (SELECT 1 FROM public.user_profiles WHERE user_id = auth.uid() AND role = 'super_admin')
);

-- STEP 6: MIGRATION FOR EXISTING USERS
-- ----------------------------------------------------------------
DO $$ 
DECLARE r RECORD;
DECLARE new_cid UUID;
BEGIN 
    -- 1. Create companies for Admins who don't have one
    FOR r IN (SELECT user_id FROM public.user_profiles WHERE role = 'admin' AND company_id IS NULL) LOOP
        INSERT INTO public.companies (name, owner_id) VALUES ('My Petrol Pump', r.user_id) 
        RETURNING id INTO new_cid;
        UPDATE public.user_profiles SET company_id = new_cid WHERE user_id = r.user_id;
    END LOOP;

    -- 2. (Optional) Assign employees to the FIRST admin if they have no company
    -- For now, we leave them NULL so they can be manually assigned or use an invite.
END $$;

