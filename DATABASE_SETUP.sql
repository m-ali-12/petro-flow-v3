-- Role checking functions
CREATE OR REPLACE FUNCTION public.get_my_role()
RETURNS TEXT LANGUAGE SQL SECURITY DEFINER STABLE SET search_path = public AS $$
  SELECT role FROM public.user_profiles WHERE user_id = auth.uid() LIMIT 1;
$$;

CREATE OR REPLACE FUNCTION public.is_active_user()
RETURNS BOOLEAN LANGUAGE SQL SECURITY DEFINER STABLE SET search_path = public AS $$
  SELECT EXISTS (SELECT 1 FROM public.user_profiles WHERE user_id = auth.uid() AND status = 'active');
$$;

CREATE OR REPLACE FUNCTION public.is_admin_or_above()
RETURNS BOOLEAN LANGUAGE SQL SECURITY DEFINER STABLE SET search_path = public AS $$
  SELECT EXISTS (SELECT 1 FROM public.user_profiles WHERE user_id = auth.uid() AND status = 'active' AND role IN ('admin','super_admin'));
$$;

-- CREATE POLICIES
CREATE POLICY "up_own_all" ON public.user_profiles FOR ALL TO authenticated USING (auth.uid() = user_id);
CREATE POLICY "up_admin_all" ON public.user_profiles FOR ALL TO authenticated USING (public.is_admin_or_above());

CREATE POLICY "customers_all" ON public.customers FOR ALL TO authenticated USING (public.is_active_user());
CREATE POLICY "transactions_all" ON public.transactions FOR ALL TO authenticated USING (public.is_active_user());
CREATE POLICY "settings_all" ON public.settings FOR ALL TO authenticated USING (public.is_active_user());
CREATE POLICY "advances_all" ON public.cash_advances FOR ALL TO authenticated USING (public.is_active_user());
CREATE POLICY "exp_cat_all" ON public.expense_categories FOR ALL TO authenticated USING (public.is_active_user());

